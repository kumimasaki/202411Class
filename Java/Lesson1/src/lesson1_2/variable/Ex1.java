package lesson1_2.variable;

public class Ex1 {

	public static void main(String[] args) {
		// 変数の初期化（宣言と代入を同時に行う）
		int age = 20;
		// ageの中身をコンソールに出力
		System.out.println(age);
		
		// 変数の中身を上書き 100
		age = 100;
		// ageの中身をコンソールに出力
		System.out.println(age);
		
		// 練習問題
		// 変数名「height」 値「160.5」で初期化
		double height = 160.5;
		System.out.println(height);
		
		// 変数名[isFlag] 値[true]
		boolean isFlag = true;
		isFlag = false;
		
	}

}
