package lesson1_3.for_statement;

/**
 * for文練習問題1,2
 */
public class Practice1 {

	public static void main(String[] args) {
		// for文練習問題1
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		// for文練習問題2
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
	}

}
